package com.example.timetable.fragments_staff;

public class Class_staff_personal
{
    private String title;
    private String text;

    public Class_staff_personal(String title, String text)
    {
        this.title = title;
        this.text = text;
    }

    public String getTitle()
    {
        return title;
    }

    public String getText()
    {
        return text;
    }
}